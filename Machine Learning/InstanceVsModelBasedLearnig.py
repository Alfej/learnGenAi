# 1. Instance-Based Learning
#   - Instance-based learning is a type of learning where the model learns from specific instances or examples in the training data.
#   - It stores the training instances and uses them to make predictions for new instances.
#   - It is called lazy learning.

# 2. Model-Based Learning
#   - Model-based learning is a type of learning where the model learns a general representation of the underlying data distribution.
#   - It builds a model that can generalize from the training data to make predictions on unseen data.
